"""Нейтрализация CSV formula/DDE-инъекции при экспорте (MEDIUM).

Строки, введённые КЛИЕНТАМИ (имена/заметки/адреса из публичных форм), при
открытии экспортного CSV в Excel/LibreOffice могут быть исполнены как формула,
если начинаются с `= + - @` или управляющего символа (`\\t`, `\\r`). Префиксуем
такие ячейки апострофом. Применять к СВОБОДНОМУ ТЕКСТУ, не к числовым колонкам
(иначе легитимный «-5» превратится в текст).
"""

_DANGEROUS_PREFIXES = ("=", "+", "-", "@", "\t", "\r")


def csv_safe(value) -> str:
    """Экранировать ячейку от formula/DDE-инъекции (для user-controlled текста)."""
    s = "" if value is None else str(value)
    if s and s[0] in _DANGEROUS_PREFIXES:
        return "'" + s
    return s
